public  final class Immutable {
	
	private String Name;
	private int RollId;
	
	
	
	
	
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public int getRollId() {
		return RollId;
	}
	public void setRollId(int rollId) {
		RollId = rollId;
	}
	

}
